     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'title' => ['required', 'string', 'max:255'],
            'duration' => ['required', 'integer'],
        ]);

        Question::create($request->all());

        return redirect()->route('admin.question.index');
    }

